#else statement with while loop
"""Else with While Loop
We can even use the else statement with the while loop. Essentially what the else statement does is that as soon as the while loop condition becomes False,
 the interpreter comes out of the while loop and the else statement is executed.

E"""
count=5
while(count>0):
    print(count)
    count=count-1
else:
    print("done")
